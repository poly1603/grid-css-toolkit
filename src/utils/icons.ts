/**
 * 创建 SVG 图标元素
 * @param name 图标名称 (lucide 图标名，用短横线分隔)
 * @param size 图标尺寸 (默认 16)
 * @param className 额外的 CSS 类名
 * @returns SVG 元素字符串
 */
export function createIcon(name: string, size: number = 16, className: string = ''): string {
  const pathData = ICON_PATHS[name];
  if (!pathData) {
    console.warn(`[GCT] Icon not found: ${name}`);
    return `<svg width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="gct-icon ${className}"><circle cx="12" cy="12" r="10"/></svg>`;
  }

  return `<svg width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="gct-icon ${className}">${pathData}</svg>`;
}

/**
 * 创建带文本的图标按钮内容
 */
export function createIconWithText(iconName: string, text: string, iconSize: number = 16): string {
  return `${createIcon(iconName, iconSize)} <span>${text}</span>`;
}

/**
 * 获取所有可用图标名称
 */
export function getAvailableIcons(): string[] {
  return Object.keys(ICON_PATHS);
}

/**
 * 注册自定义图标
 */
export function registerIcon(name: string, svgPath: string): void {
  ICON_PATHS[name] = svgPath;
}

/**
 * 批量注册图标
 */
export function registerIcons(icons: Record<string, string>): void {
  Object.assign(ICON_PATHS, icons);
}
